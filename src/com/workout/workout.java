package com.workout;

public class workout {

	private int UID;
	private int workout_id;
	private String workout_type;
	private int workout_length; 
	private String workout_measure;
	
	
	
	public int getUID() {
		return UID;
	}
	public void setUID(int uID) {
		this.UID = uID;
	}
	public int getWorkout_id() {
		return workout_id;
	}
	public void setWorkout_id(int workout_id) {
		this.workout_id = workout_id;
	}
	public String getWorkout_type() {
		return workout_type;
	}
	public void setWorkout_type(String workout_type) {
		this.workout_type = workout_type;
	}
	public int getWorkout_length() {
		return workout_length;
	}
	public void setWorkout_length(int workout_length) {
		this.workout_length = workout_length;
	}
	public String getWorkout_measure() {
		return workout_measure;
	}
	public void setWorkout_measure(String workout_measure) {
		this.workout_measure = workout_measure;
	}
	
}
